def _generate_version() -> str:
    """Generate a UTC-timestamp version string.

    Returns:
        Version string in ``v<YYYYMMDD_HHMMSS>`` format.

    Example::

        >>> v = _generate_version()
        >>> assert v.startswith("v") and len(v) == 16
    """
    return datetime.now(tz=timezone.utc).strftime("v%Y%m%d_%H%M%S")


def _get_git_commit_hash() -> str | None:
    """Return the short git commit hash of HEAD, or None on failure.

    Returns:
        7-character hex string or ``None`` if git is unavailable.

    Example::

        >>> h = _get_git_commit_hash()
        >>> assert h is None or len(h) == 7
    """
    try:
        result = subprocess.run(
            ["git", "rev-parse", "--short", "HEAD"],
            capture_output=True,
            text=True,
            check=True,
            timeout=5,
        )
        return result.stdout.strip()
    except Exception:  # noqa: BLE001  # pylint: disable=broad-exception-caught
        return None


def _compute_normalization_stats(
    vectors: list[dict[str, float]],
) -> tuple[dict[str, float], dict[str, float]]:
    """Compute per-feature mean and standard deviation from a list of vectors.

    Zero-variance features receive ``std = 0.0`` in the output (the
    :func:`~src.grading._utils._normalize_vector` function applies a
    ``std = 1.0`` fallback at inference time).

    Args:
        vectors: Non-empty list of feature dicts, all sharing the same keys.

    Returns:
        Tuple of ``(mean_dict, std_dict)`` keyed by feature name.

    Example::

        >>> mean, std = _compute_normalization_stats([{"x": 1.0}, {"x": 3.0}])
        >>> mean["x"]
        2.0
        >>> std["x"]
        1.0
    """
    n = len(vectors)
    keys = list(vectors[0].keys())
    mean: dict[str, float] = {}
    std: dict[str, float] = {}
    for k in keys:
        values = [v[k] for v in vectors]
        mu = math.fsum(values) / n
        # Population variance (÷N, not ÷N-1). Intentional: the same stats are
        # applied at both training and inference time, so the choice of N vs N-1
        # only affects the scale of normalised values, not model correctness.
        variance = math.fsum((val - mu) ** 2 for val in values) / n
        mean[k] = mu
        std[k] = math.sqrt(variance)
    return mean, std


def _make_synthetic_hold(
    hold_id: int,
    x: float,
    y: float,
    rng: random.Random,
) -> ClassifiedHold:
    """Create a ClassifiedHold at position (x, y) with randomised type.

    Args:
        hold_id: Non-negative hold identifier.
        x: Normalised x-center [0, 1].
        y: Normalised y-center [0, 1].
        rng: Seeded random instance for reproducibility.

    Returns:
        A validated :class:`~src.graph.types.ClassifiedHold`.
    """
    hold_type = rng.choice(HOLD_CLASSES)
    type_confidence = rng.uniform(0.5, 0.99)
    remaining = 1.0 - type_confidence
    other_n = len(HOLD_CLASSES) - 1
    type_probabilities = {
        c: type_confidence if c == hold_type else remaining / other_n
        for c in HOLD_CLASSES
    }
    width = rng.uniform(0.04, 0.12)
    height = rng.uniform(0.04, 0.12)
    return ClassifiedHold(
        hold_id=hold_id,
        x_center=x,
        y_center=y,
        width=width,
        height=height,
        detection_class="hold",
        detection_confidence=rng.uniform(0.6, 0.99),
        hold_type=hold_type,
        type_confidence=type_confidence,
        type_probabilities=type_probabilities,
    )


