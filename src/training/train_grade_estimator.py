"""XGBoost grade estimator training for bouldering routes.

Trains an ordinal multi-class :class:`~xgboost.XGBClassifier` to predict
V-scale grades (V0–V17) from :class:`~src.features.assembler.RouteFeatures`.

Typical workflow::

    from src.training.train_grade_estimator import (
        generate_synthetic_training_data,
        train_grade_estimator,
    )

    samples = generate_synthetic_training_data(n_samples=2000, seed=42)
    features, labels = zip(*samples)
    result = train_grade_estimator(list(features), list(labels), Path("models/grading"))
    print(result.version, result.metrics.val_accuracy)

The trained model is saved to ``output_dir/<version>/`` containing:

* ``model.pkl`` — serialised ``XGBClassifier`` (via :mod:`joblib`)
* ``metadata.json`` — training provenance, normalization stats, and metrics

The model can then be used via :func:`~src.grading.ml_estimator.estimate_grade_ml`.
"""

