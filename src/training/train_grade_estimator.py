# ---------------------------------------------------------------------------
# Constants
# ---------------------------------------------------------------------------

_N_GRADES = len(V_GRADES)  # 18
_MIN_TRAINING_SAMPLES = 10

# Fixed 3×4 spatial grid for synthetic route generation.
# Spacing: 0.25 (horizontal), 0.20 (vertical).
# Diagonal distance ≈ 0.32 < BASE_REACH_RADIUS (0.35) → edges always form.
_GRID_POSITIONS: list[tuple[float, float]] = [
    (x, y) for y in (0.20, 0.40, 0.60, 0.80) for x in (0.25, 0.50, 0.75)
]  # 12 positions total

# ---------------------------------------------------------------------------
# Result models
# ---------------------------------------------------------------------------


class GradeTrainingMetrics(BaseModel):
    """Metrics produced during grade estimator training.

    Attributes:
        train_accuracy: Accuracy on the training split, in ``[0, 1]``.
        val_accuracy: Accuracy on the validation split, in ``[0, 1]``.
        mean_absolute_error: Mean absolute error in grade indices on the
            validation split (ordinal-aware metric).
    """

    train_accuracy: float = Field(ge=0.0, le=1.0)
    val_accuracy: float = Field(ge=0.0, le=1.0)
    mean_absolute_error: float = Field(ge=0.0)


class GradeTrainingResult(BaseModel):
    """Result of a grade estimator training run.

    Attributes:
        version: Version string in ``v<YYYYMMDD_HHMMSS>`` format.
        model_path: Absolute path to the saved ``model.pkl``.
        metadata_path: Absolute path to the saved ``metadata.json``.
        metrics: Training and validation metrics.
        n_samples: Total number of training samples used.
        feature_names: Ordered list of feature names (34 entries).
        data_source: ``"synthetic"`` if labels were generated by the
            heuristic estimator; ``"real"`` if from a labelled dataset.
        git_commit: Short git commit hash at training time, or ``None``
            if not available.
        trained_at: ISO-8601 UTC timestamp of training completion.
        hyperparameters: XGBoost hyperparameters used for training.
    """

    version: str
    model_path: Path
    metadata_path: Path
    metrics: GradeTrainingMetrics
    n_samples: int = Field(ge=1)
    feature_names: list[str]
    data_source: Literal["synthetic", "real"]
    git_commit: str | None
    trained_at: str
    hyperparameters: dict[str, Any]


